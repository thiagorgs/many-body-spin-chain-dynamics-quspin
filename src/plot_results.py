"""
Plot magnetization dynamics generated by run_quspin_dynamics.py.
"""

from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np


def main() -> None:
    root = Path(__file__).resolve().parents[1]
    results_path = root / "results" / "quspin_dynamics_results.npz"
    figures_dir = root / "figures"
    figures_dir.mkdir(exist_ok=True)

    if not results_path.exists():
        raise FileNotFoundError(
            f"Results file not found: {results_path}\n"
            "Run `python src/run_quspin_dynamics.py` first."
        )

    data = np.load(results_path)

    times = data["times"]
    local_mz = data["local_mz"]
    average_mz = data["average_mz"]
    length = int(data["length"])
    disorder_strength = float(data["disorder_strength"])
    h_field = float(data["h_field"])

    plt.figure(figsize=(7, 4.5))
    plt.plot(times, average_mz, linewidth=2)
    plt.xlabel("Time")
    plt.ylabel(r"$M_z(t)$")
    plt.title(
        rf"Average Magnetization: $L={length}$, "
        rf"$W={disorder_strength}$, $h={h_field}$"
    )
    plt.tight_layout()

    output_path = figures_dir / "average_magnetization.png"
    plt.savefig(output_path, dpi=300)
    plt.close()

    plt.figure(figsize=(7, 4.5))

    for site in range(length):
        plt.plot(times, local_mz[:, site], linewidth=1.2, label=rf"$i={site}$")

    plt.xlabel("Time")
    plt.ylabel(r"$\langle Z_i(t)\rangle$")
    plt.title(r"Local Magnetization Dynamics")
    plt.legend(ncol=2, fontsize=8)
    plt.tight_layout()

    output_path_local = figures_dir / "local_magnetization.png"
    plt.savefig(output_path_local, dpi=300)
    plt.close()

    print(f"Saved figure: {output_path}")
    print(f"Saved figure: {output_path_local}")


if __name__ == "__main__":
    main()
